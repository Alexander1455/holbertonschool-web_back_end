#!/usr/bin/env python3
"""
Convert a float to its string representation.

Args:
    n (float): The input floating-point number.

Returns:
str: The string representation of the input number.

Example:
    >>> to_str(5.7)
     '5.7'
    >>> to_str(9.2)
    '9.2'
"""


def to_str(n: float) -> str:
    return str(n)
